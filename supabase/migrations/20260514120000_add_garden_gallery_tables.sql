create table if not exists public.garden_gallery_entries (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  note text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists garden_gallery_entries_user_id_idx on public.garden_gallery_entries (user_id);
create index if not exists garden_gallery_entries_created_at_idx on public.garden_gallery_entries (created_at desc);

create table if not exists public.garden_gallery_photos (
  id bigint generated by default as identity primary key,
  entry_id bigint not null references public.garden_gallery_entries(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  photo_url text not null,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

create index if not exists garden_gallery_photos_entry_id_idx on public.garden_gallery_photos (entry_id);
create index if not exists garden_gallery_photos_user_id_idx on public.garden_gallery_photos (user_id);

create table if not exists public.garden_gallery_entry_plants (
  id bigint generated by default as identity primary key,
  entry_id bigint not null references public.garden_gallery_entries(id) on delete cascade,
  plant_id bigint not null references public.plants(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint garden_gallery_entry_plants_unique unique (entry_id, plant_id)
);

create index if not exists garden_gallery_entry_plants_entry_id_idx on public.garden_gallery_entry_plants (entry_id);
create index if not exists garden_gallery_entry_plants_plant_id_idx on public.garden_gallery_entry_plants (plant_id);
create index if not exists garden_gallery_entry_plants_user_id_idx on public.garden_gallery_entry_plants (user_id);

alter table public.garden_gallery_entries enable row level security;
alter table public.garden_gallery_photos enable row level security;
alter table public.garden_gallery_entry_plants enable row level security;


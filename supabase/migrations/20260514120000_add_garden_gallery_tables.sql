drop policy if exists "garden_gallery_entry_plants_insert_own" on public.garden_gallery_entry_plants;
create policy "garden_gallery_entry_plants_insert_own"
on public.garden_gallery_entry_plants
for insert
to authenticated
with check (
  auth.uid() = user_id
  and exists (
    select 1
    from public.garden_gallery_entries
    where garden_gallery_entries.id = entry_id
      and garden_gallery_entries.user_id = auth.uid()
  )
  and exists (
    select 1
    from public.plants
    where plants.id = plant_id
      and plants.user_id = auth.uid()
  )
);

